function placeExample(name) {    
    reset()
    
    exmp = examples[name]
    var cornerX = int(cols/2-exmp[0].length/2)
    var cornerY = int(rows/2-exmp.length/2)
    for (x = 0; x < exmp[0].length; x++) {
        for (y = 0; y < exmp.length; y++) {
            cells[cornerX+x][cornerY+y] = exmp[y][x];
        }
    }
}

